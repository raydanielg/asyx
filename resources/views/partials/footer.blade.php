    <script>
    (function() {
        const form = document.getElementById('footerNewsletterForm');
        const btn = document.getElementById('footerNlBtn');
        const btnText = document.getElementById('fNlBtnText');
        const btnIcon = document.getElementById('fNlBtnIcon');
        const btnSpinner = document.getElementById('fNlBtnSpinner');
        const successDiv = document.getElementById('footerNewsletterSuccess');
        const errorDiv = document.getElementById('footerNewsletterError');

        form.addEventListener('submit', async function(e) {
            e.preventDefault();
            successDiv.classList.add('hidden');
            errorDiv.classList.add('hidden');

            btn.disabled = true;
            btnText.textContent = 'Subscribing...';
            btnIcon.classList.add('hidden');
            btnSpinner.classList.remove('hidden');

            const formData = new FormData(form);

            try {
                const response = await fetch('{{ route("newsletter.subscribe") }}', {
                    method: 'POST',
                    headers: {
                        'X-CSRF-TOKEN': formData.get('_token'),
                        'Accept': 'application/json',
                    },
                    body: formData
                });

                const data = await response.json();

                if (response.ok && data.success) {
                    successDiv.classList.remove('hidden');
                    form.reset();
                } else {
                    throw new Error(data.message || 'Failed');
                }
            } catch (err) {
                errorDiv.classList.remove('hidden');
            } finally {
                btn.disabled = false;
                btnText.textContent = 'Subscribe';
                btnIcon.classList.remove('hidden');
                btnSpinner.classList.add('hidden');
            }
        });
    })();
    </script>
